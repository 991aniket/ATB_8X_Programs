package Practice_Prgramms;

public class Lab_13_Ternary_Operator {
    public static void main(String[] args) {
        // Ternary operator
        // Syntax - condition ? expression1 : expression2
        int a = 10;
        int b = 30;
        int result = (a>b)?a:b; //
        System.out.println(result);
    }
}
