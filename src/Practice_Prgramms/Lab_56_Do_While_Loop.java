package Practice_Prgramms;

public class Lab_56_Do_While_Loop {
    public static void main(String[] args) {

//        /  While -> int -> condition -> body -> increment / decrement
//        // Do while  -> // int -> body -> condition -> increment / decrement

        int a= 1;
        do {
            System.out.println(a);
            // In Do While first Statement will print then
            // condition will check then increment/decrement will happen
            a++;
        }while (a<10);


    }
}
