package Practice_Prgramms;

public class Lab_16_Increment_Operator {
    public static void main(String[] args) {
        //Pre
        int a = 10;
        int result_1 = ++a;
        System.out.println(a);
        System.out.println(result_1);
        //Post
        int b = 10;
        int result_2 = b++;
        System.out.println(result_2);
        System.out.println(b);


    }
}
