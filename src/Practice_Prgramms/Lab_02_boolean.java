package Practice_Prgramms;

public class Lab_02_boolean
{
    public static void main (String[] args)
    {
        //Data type variable_name = variable_value
        boolean is_married=true;
        System.out.println(is_married);

        // Variable_name is Identifier
        // Rules
        // 1.It cannot start with number
        // 2.character spacing is not allowed
        // 3.It should not be keyword/reserv word
        // 4.It can use only two special character _ & $
        // 5.It can be of any Length
        boolean $ =false;
        boolean a_p_9_$ =true;
        System.out.println(a_p_9_$);
        System.out.println($);
    }
}
