package Practice_Prgramms;

public class Lab_10_Logical_Operators {
    public static void main(String[] args) {

        //logical operators

        // ! -> NOT
        // && -> And
        // || -> OR
        boolean a = true;
        boolean b = false;

        System.out.println(a && b);
        System.out.println(a || b);
        System.out.println(!a);
        System.out.println(!b);


    }
}
