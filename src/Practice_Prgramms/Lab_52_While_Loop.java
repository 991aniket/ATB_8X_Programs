package Practice_Prgramms;

public class Lab_52_While_Loop {
    public static void main(String[] args) {
        // while
        // while - A, B, C
        //         // A - Initialize first but outside while
        //        // B - Condition
        //        // C - Increment / Decrement
        int a =1;
        while (a<3)
        {
            System.out.println(a);
            a++;
        }

    }
}
